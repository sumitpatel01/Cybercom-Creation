<?php

//this is assignment operator in php.
$number1 = 10;
$number2 = 2;

// greater than
if($number1 >= $number2){
	echo 'true';
}else{
	echo 'false';
}

// equal
if($number1 == $number2){
	echo 'true';
}else{
	echo 'false';
}

// not equal
if($number1 != $number2){
	echo 'true';
}else{
	echo 'false';
}

// less than
if($number1 <= $number2){
	echo 'true';
}else{
	echo 'false';
}



?>